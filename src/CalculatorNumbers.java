public class CalculatorNumbers {

    private int a;
    private int b;
    private String operator;


    CalculatorNumbers() {

    }

    public int getA() {
        return a;
    }
    public int getB() {
        return b;
    }
    public String getOperator() {
        return operator;
    }
    public void setA(int a) {
        this.a = a;
    }
    public void setB(int b) {
        this.b = b;
    }
    public void setOperator(String operator) {
        this.operator = operator;
    }
}
